export interface TerminalColors {
  background: string
  foreground: string
  cursor: string
  selectionBackground: string
  black: string; red: string; green: string; yellow: string
  blue: string; magenta: string; cyan: string; white: string
  brightBlack: string; brightRed: string; brightGreen: string
  brightYellow: string; brightBlue: string; brightMagenta: string
  brightCyan: string; brightWhite: string
}

export interface ColorTheme {
  id: string
  name: string
  /** Preview colors: [background, sidebar, accent, foreground] */
  preview: [string, string, string, string]
  vars: Record<string, string>
  terminal: TerminalColors
}

const KEY = "huxflux:color-theme"

export function getColorTheme(): string {
  return localStorage.getItem(KEY) ?? "stone"
}

export function setColorTheme(id: string) {
  localStorage.setItem(KEY, id)
  applyColorTheme(id)
  window.dispatchEvent(new CustomEvent("huxflux:color-theme-change", { detail: id }))
}

export function applyColorTheme(id: string) {
  const theme = colorThemes.find((t) => t.id === id)
  if (!theme) return
  const root = document.documentElement
  for (const [key, value] of Object.entries(theme.vars)) {
    root.style.setProperty(key, value)
  }
}

